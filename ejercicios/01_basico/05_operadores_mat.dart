/** 
 * un operador es un simbolo que 
 * le dice al compilador que 
 * debe de realizar una tarea matematica,
 * relacional o logica 
 * y debe producir un resultado 
 * */
main() {
  //operadores aritmeticos
  int a = 10 + 5; //+ es el operador
  int b = 10 - 5; //- es el operador
  int c = 10 * 5; //* es el operador

  double d = 10 / 2; // / es el operador

  int e = 10 % 3; // % division sintetica
  //print(e);

  b = -b; // -expr simbolo de negacion
  int f = 10 ~/ 3; //~/ division comun y corriente pero solo el entero

  int g = 1;
  g++; // ++ incrementa en 1
  g--; // disminuye en 1
  g += 2; //suma 3
}
